#include <stdio.h>
#include <stdlib.h>


int find_min_p (int x, int b) {
    int sum = 0;
    int p = 0;
    
    if (x == 0) {
        printf("[ERROR] X must > 0\n");
        return -1; // error*/
    }

    if (b == 0 && x > b) {
        return 0;
    }
       
    int temp = 1;

    while (sum <= b) {
        p++;
        temp *= x;
        sum += temp;
    }

    return p;
}


void main (void) {
    int x, b, res;

    printf("put X: ");
    scanf("%i", &x);

    printf("put B: ");
    scanf("%i", &b);
    
    res = find_min_p(x, b);
    printf("Min P: %i\n", res);
}

